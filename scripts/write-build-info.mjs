#!/usr/bin/env node
import { execSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const target = path.join(root, 'src/lib/build-info.ts');

function getEdition() {
  try {
    return execSync('git rev-parse --short HEAD', { cwd: root, encoding: 'utf8' }).trim();
  } catch {
    return 'local';
  }
}

const edition = getEdition();
const builtAt = new Date().toISOString();

const content = `// Generated by scripts/write-build-info.mjs — do not edit
export const SITE_EDITION = ${JSON.stringify(edition)};
export const SITE_BUILT_AT = ${JSON.stringify(builtAt)};
`;

writeFileSync(target, content);
console.log(`edition  ${edition}`);
